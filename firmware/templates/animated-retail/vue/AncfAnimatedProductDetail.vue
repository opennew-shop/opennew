    <footer class="action-bar">
      <div>
        <span>Total preview</span>
        <strong>{{ formattedPrice }}</strong>
      </div>
      <button type="button" class="secondary" @click="emitSelect">Select</button>
      <button type="button" class="primary" @click="emitQuote">Request Quote</button>
    </footer>
  </main>
