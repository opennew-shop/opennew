<script setup lang="ts">
import { computed, ref } from 'vue';

type Price = {
  currency: string;
  amount_minor: string;
  scale: number;
};

export type AncfProductDetail = {
  sku_id: string;
  title: string;
  description?: string;
  price: Price;
  stock_hint?: number;
  specs?: Record<string, string>;
  media?: { thumbnail?: string; gallery?: string[] };
};

const props = withDefaults(defineProps<{
  product: AncfProductDetail;
  shopLabel?: string;
  manifestStatus?: string;
  protocolVersion?: string;
  theme?: Record<string, string>;
}>(), {
  shopLabel: 'ANCF Shop',
  manifestStatus: 'manifest active',
  protocolVersion: 'ANCF-1.0',
  theme: () => ({}),
});

const emit = defineEmits<{
  select: [item: AncfProductDetail];
  quote: [item: AncfProductDetail];
  back: [];
}>();

const activeImage = ref(0);

const cssVars = computed(() => ({
  '--ancf-primary': props.theme.primary || '#00FFA3',
  '--ancf-background': props.theme.background || '#F8FAFC',
  '--ancf-text': props.theme.text || '#111827',
  '--ancf-surface': props.theme.surface || '#FFFFFF',
  '--ancf-border': props.theme.border || '#E5E7EB',
  '--ancf-muted': props.theme.muted || '#6B7280',
  '--ancf-warning': props.theme.warning || '#FFAA00',
}));

const gallery = computed(() => {
  const images = props.product.media?.gallery?.length
    ? props.product.media.gallery
    : props.product.media?.thumbnail
      ? [props.product.media.thumbnail]
      : [];
  return images.filter(Boolean);
});

const heroImage = computed(() => gallery.value[activeImage.value] || props.product.media?.thumbnail || '');

const formattedPrice = computed(() => {
  const price = props.product.price;
  const value = Number(BigInt(price.amount_minor)) / 10 ** price.scale;
  return `${value.toFixed(Math.min(price.scale, 6))} ${price.currency}`;
});

const stockText = computed(() => {
  const stock = props.product.stock_hint;
  if (stock === undefined) return 'stock hidden';
  if (stock === 0) return 'out of stock';
  if (stock < 10) return `${stock} left`;
  return `${stock} available`;
});

function emitSelect(): void {
  emit('select', props.product);
}

function emitQuote(): void {
  emit('quote', props.product);
}
</script>

